export default function Knit(props) {
    let RenderPattern;
    let renderString;
    let viewBoxString;
    if (props.gender === 0) {
        renderString = 'Male';
    }
    if (props.gender === 1) {
        renderString = 'Female';
    }
    if (props.flip === 0) {
        renderString = renderString.concat('Front');
        viewBoxString = "0 0 734.66669 774.66669"
    }
    if (props.flip === 1) {
        renderString = renderString.concat('Back');
        viewBoxString = "0 0 551 580"
    }
    renderString = renderString.concat(props.currentPattern);
    RenderPattern = patterns[renderString];
    return (
        <RenderPattern width={props.width} height={props.height} viewBox={viewBoxString} fill={props.fill} fillSecondary={props.fillSecondary} fillThird={props.fillThird} stroke={props.stroke} strokeWidth={props.strokeWidth}/>
    )
}